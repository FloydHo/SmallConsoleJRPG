        private void DrawBattleTransition()
        {
            Console.ForegroundColor = ConsoleColor.DarkGray;
            Console.SetCursorPosition(1, 1);
            for (int y = 0; y < map.GetLength(0); y++)
            {
                for (int x = 0; x < map.GetLength(1); x++)
                {
                    Console.Write($"#");
                }
                Console.SetCursorPosition(1, 1 + y);
                Thread.Sleep(15);
            }
        }

        private void PlayerCharacter()
        {
            Console.ForegroundColor = ConsoleColor.Yellow;
            char playerSymbol = '@';
            Console.SetCursorPosition(playerPosition[0], playerPosition[1]);
            Console.Write(playerSymbol);
            Console.ForegroundColor = ConsoleColor.White;
        }

        private void DrawCircle(int h, int k, int r)
        {
            for (int y = -r; y <= r; y++)
            {
                for (int x = -r; x <= r; x++)
                {
                    if (x * x + y * y <= r * r)
                    {
                        if (h + x < map.GetLength(1) && h + x > 0 && k + y < map.GetLength(0) && k + y > 0)
                        {
                            Console.SetCursorPosition(h + x, k + y);
                            Console.Write(map[k + y, h + x]);

                        }
                    }
                }
            }
        }

        private void MovePlayer()
        {
            switch (Console.ReadKey(true).Key)
            {
                case ConsoleKey.UpArrow:
                    if (map[playerPosition[1] - 1, playerPosition[0]] != '#')
                        playerPosition[1] -= 1;
                    break;
                case ConsoleKey.DownArrow:
                    if (playerPosition[1] + 1 < map.GetLength(0))
                        if (map[playerPosition[1] + 1, playerPosition[0]] != '#')
                            playerPosition[1] += 1;
                    break;
                case ConsoleKey.RightArrow:
                    if (map[playerPosition[1], playerPosition[0] + 1] != '#')
                        playerPosition[0] += 1;
                    break;
                case ConsoleKey.LeftArrow:
                    if (map[playerPosition[1], playerPosition[0] - 1] != '#')
                        playerPosition[0] -= 1;
                    break;
                default:
                    break;
            }
        }



